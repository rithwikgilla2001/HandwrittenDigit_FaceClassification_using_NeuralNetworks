    inp = np.dot(data, w1.T)
    inp_1 = sigmoid(inp)

    inp_1 = np.append(inp_1, np.ones((inp_1.shape[0], 1)), axis=1)


    out_l = np.dot(inp_1, w2.T)
    out_fin = sigmoid(out_l)

    labels = np.argmax(out_fin, axis=1)

    return labels

